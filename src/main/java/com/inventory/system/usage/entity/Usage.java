package com.inventory.system.usage.entity;

import java.time.LocalDateTime;

import com.inventory.system.item.entity.Item;
import com.inventory.system.user.entity.User;

import jakarta.persistence.*;

@Entity
@Table(name = "usage_records")
public class Usage {

	@Id
	@GeneratedValue(strategy = GenerationType.IDENTITY)
	private Long id;

	@ManyToOne
	@JoinColumn(name = "item_id")
	private Item item;

	private Double quantity;

	private String department;

	private LocalDateTime usedDateTime;

	private String takenBy;

	private String givenBy;

	@Column(name = "cost_per_unit")
	private Double costPerUnit;

	@Column(name = "total_cost")
	private Double totalCost;

	@ManyToOne
	@JoinColumn(name = "created_by")
	private User createdBy;

	private LocalDateTime createdAt;

	@ManyToOne
	@JoinColumn(name = "updated_by")
	private User updatedBy;

	private LocalDateTime updatedAt;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Item getItem() {
		return item;
	}

	public void setItem(Item item) {
		this.item = item;
	}

	public Double getQuantity() {
		return quantity;
	}

	public void setQuantity(Double quantity) {
		this.quantity = quantity;
	}

	public String getDepartment() {
		return department;
	}

	public void setDepartment(String department) {
		this.department = department;
	}

	public LocalDateTime getUsedDateTime() {
		return usedDateTime;
	}

	public void setUsedDateTime(LocalDateTime usedDateTime) {
		this.usedDateTime = usedDateTime;
	}

	public String getTakenBy() {
		return takenBy;
	}

	public void setTakenBy(String takenBy) {
		this.takenBy = takenBy;
	}

	public String getGivenBy() {
		return givenBy;
	}

	public void setGivenBy(String givenBy) {
		this.givenBy = givenBy;
	}

	public User getCreatedBy() {
		return createdBy;
	}

	public void setCreatedBy(User createdBy) {
		this.createdBy = createdBy;
	}

	public LocalDateTime getCreatedAt() {
		return createdAt;
	}

	public void setCreatedAt(LocalDateTime createdAt) {
		this.createdAt = createdAt;
	}

	public User getUpdatedBy() {
		return updatedBy;
	}

	public void setUpdatedBy(User updatedBy) {
		this.updatedBy = updatedBy;
	}

	public LocalDateTime getUpdatedAt() {
		return updatedAt;
	}

	public void setUpdatedAt(LocalDateTime updatedAt) {
		this.updatedAt = updatedAt;
	}

	public Double getCostPerUnit() {
		return costPerUnit;
	}

	public void setCostPerUnit(Double costPerUnit) {
		this.costPerUnit = costPerUnit;
	}

	public Double getTotalCost() {
		return totalCost;
	}

	public void setTotalCost(Double totalCost) {
		this.totalCost = totalCost;
	}

}